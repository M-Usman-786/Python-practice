course = 'My name is Muhammad Usman'
#len counts the string alphabats
print(len(course))

#convert the string into upper case
print(course.upper())

#convert the string into lower alphabats
print(course.lower())
print(course)

#find the index of string element
print(course.find('u'))

#replace the character
print(course.replace('Usman','Usman Khalid'))
print(course.replace('M', 'yo'))

#in function will give the true or false value after checking the condition
print('Usman' in course)
